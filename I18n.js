// Joriy til (localStorage dan)
var currentLang = localStorage.getItem('nlx_lang') || 'uz';

function t(key) {
  return (LANGS[currentLang] && LANGS[currentLang][key]) || (LANGS['uz'] && LANGS['uz'][key]) || key;
}

function setLang(lang) {
  if (!LANGS[lang]) return;
  currentLang = lang;
  localStorage.setItem('nlx_lang', lang);
  applyLang();
  // Til tugmalarini yangilash
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function applyLang() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.dataset.i18n;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = t(key);
    } else if (el.dataset.i18nAttr) {
      el.setAttribute(el.dataset.i18nAttr, t(key));
    } else {
      el.innerHTML = t(key);
    }
  });
  // html lang attributi
  document.documentElement.lang = currentLang;
  // Bot tilini yangilash
  if (typeof updateBotLang === 'function') updateBotLang();
}

// Til tugmachalari HTML
function renderLangSwitcher() {
  return '<div class="lang-switcher">' +
    ['uz','en','ru'].map(function(l) {
      return '<button class="lang-btn' + (l === currentLang ? ' active' : '') +
        '" data-lang="' + l + '" onclick="setLang(\'' + l + '\')">' + l.toUpperCase() + '</button>';
    }).join('') +
  '</div>';
}